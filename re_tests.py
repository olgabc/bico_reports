import re

mytext2 = r'1700-g05-к-y03-00490-2019 85-огэ-2018 выполнение работ по замерам контуров заземления'
pat2 = r'^([^A-zА-я]*[A-zА-я]{,3}[^A-zА-я]+)+((поставка|закупка|приобретение|оказание услуг|услуги|выполнение работ|ремонт|текущий ремонт|ниокр|смр|строительство)[.]*)'
replace2 = re.sub(pat2, r'\2', mytext2)
print(replace2)

